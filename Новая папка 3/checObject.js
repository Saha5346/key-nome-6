function checkEmptyObject(obj) {
    // Проверяем, является ли переданный аргумент объектом
    if (typeof obj !== 'object' || obj === null) {
        console.error('Пожалуйста, передайте объект для проверки.');
        return;
    }
    
    // Получаем массив ключей объекта
    const keys = Object.keys(obj);
    
    // Проверяем, пуст ли объект (нет ключей)
    if (keys.length === 0) {
        console.log(' true '); // Выводим сообщение о пустом объекте
    } else {
        console.log(' false'); // Выводим сообщение о том, что объект не пустой
    }
}

// Пример использования функции
const emptyObject = {};
const nonEmptyObject = { key: 'value' };

// Вызываем функцию для проверки пустого объекта
checkEmptyObject(emptyObject); // Выведет: "Объект пустой."

// Вызываем функцию для проверки не пустого объекта
checkEmptyObject(nonEmptyObject); // Выведет: "Объект содержит ключи."
